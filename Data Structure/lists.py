fruits = ["apple", "banana", "cherry"]
print(fruits[0])   # apple

fruits.append("orange")     # Add item

fruits.remove("banana")     # Remove item

fruits.insert(1, "mango")   # Insert at index

fruits.pop(2)                # Remove item

print(fruits)
