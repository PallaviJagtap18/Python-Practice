# Operators in Python

# Operators are special symbols used to perform operations on variables and values.
# Python provides different types of operators such as arithmetic, comparison, logical, and assignment operators.

# 1. Arithmetic Operators

# Arithmetic operators are used to perform mathematical calculations like addition, subtraction, multiplication, etc.

a = 10
b = 3

print("Addition: ", a + b )
print("Subtraction: ", a - b )
print("Multiplication: ", a * b )
print("Division: ", a / b )
print("Modulus: ", a % b )
print("Power: ", a ** b )
print("floor division: ", a // b )

