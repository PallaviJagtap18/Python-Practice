# For Loop in Python

# A for loop in Python is used to iterate (repeat) over a sequence of items such as a list, string, tuple, range, or dictionary. It executes a block of code once for each element in the sequence.

# Example 1

for i in range(1, 11):
    print(i)


# Example 3

#Using Step in range()
for i in range(1, 20 , 3):
    print(i)

# Example 3

fruits = ["apple", "Banana","Mango","Watermelon"]
